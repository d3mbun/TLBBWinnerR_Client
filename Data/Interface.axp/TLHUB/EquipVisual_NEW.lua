	if IsWindowShow("EquipVisual_NEW") then
		if event == "UI_COMMAND" and tonumber(arg0) == 10102017 then
			EquipVisual_NEW_Update(tonumber(arg1))
		end
	end
	
	if event == "PLAYER_LEAVE_WORLD" then
		EquipVisual_NEW_OnHidden();
		return;
	end
end

function EquipVisual_NEW_Update(value)
	local TheAction = EnumAction(value, "packageitem");
	local ItemID = TheAction:GetDefineID()
	if ItemID >= 10300000 and ItemID <= 10305444 then
		if not EquipVisual_NEW_ListSW[ItemID] then
			PushDebugMessage("Chï có Thßþng C± Th¥n Khí m¾i có th¬ tiªn hành!")
			return
		end
		EquipVisual_NEW_Object:SetActionItem(TheAction:GetID())
		if EquipVisual_NEW_Index ~= -1 then
			LifeAbility:Lock_Packet_Item( EquipVisual_NEW_Index, 0 )
			EquipVisual_NEW_Index = value
			LifeAbility:Lock_Packet_Item( value, 1 )
		else	
			LifeAbility:Lock_Packet_Item( value, 1 )
			EquipVisual_NEW_Index = value
		end
		EquipVisual_NEW_Selection = -1;
		EquipVisual_NEW_UpdateUI()
	elseif ItemID == 30900004 then
		EquipVisual_NEW_ObjectMaterial:SetActionItem(TheAction:GetID())
		LifeAbility:Lock_Packet_Item( value, 1 )
		EquipVisual_NEW_Material = value
	end
	
	if EquipVisual_NEW_Index == -1 then
		EquipVisual_NEW_ListModel:ClearListBox()
		EquipVisual_NEW_FakeObject:SetFakeObject("");
	else
		if ItemID ~= 30900004 then
			EquipVisual_NEW_FakeObject:SetFakeObject("");
			if EquipVisual_NEW_ListSW[ItemID] then
				EquipVisual_NEW_ListModel:ClearListBox()
				for i = 1, table.getn(EquipVisual_NEW_ListSW[ItemID]) do
					EquipVisual_NEW_ListModel:AddItem(" - "..EquipVisual_NEW_ListSW[ItemID][i],i-1);
				end
				
				local Visual_ID = LifeAbility:Get_Equip_Exterior(EquipVisual_NEW_Index,0);
				LifeAbility:Wear_Equip_VisualID(EquipVisual_NEW_Index,Visual_ID)
				
				EquipVisual_NEW_FakeObject:SetFakeObject("")
				EquipVisual_NEW_FakeObject:SetFakeObject("EquipChange_Player");
				FakeObj_SetCamera( "EquipChange_Player", 2, 9)
			else
				EquipVisual_NEW_ListModel:ClearListBox()
			end
		end
	end
end

function EquipVisual_NEW_Selected()
	local nSelIndex = EquipVisual_NEW_ListModel:GetFirstSelectItem();
	if nSelIndex == -1 then
		return
	end
	
	EquipVisual_NEW_Selection = nSelIndex;
	EquipVisual_NEW_UpdateUI()
	
	local Visual_ID = LifeAbility:Get_Equip_Exterior(EquipVisual_NEW_Index,nSelIndex);
	LifeAbility:Wear_Equip_VisualID(EquipVisual_NEW_Index,Visual_ID)
	
	EquipVisual_NEW_FakeObject:SetFakeObject("")
	EquipVisual_NEW_FakeObject:SetFakeObject("EquipChange_Player");
	FakeObj_SetCamera( "EquipChange_Player", 2, 9)
end

function EquipVisual_NEW_Buttons_Clicked()
	if EquipVisual_NEW_Index == -1 then
		PushDebugMessage("Vui lòng ð¬ nguyên li®u vào ô bên dß¾i!")
		return
	end
	
	local Money = tostring(Player:GetData("MONEY"))
	local MoneyJZ = tostring(Player:GetData("MONEY_JZ"))
	
	if Money + MoneyJZ < EquipVisual_NEW_NeedMoney then
		PushDebugMessage("Không ðü ngân lßþng.")
		return
	end
	
	if EquipVisual_NEW_Index == -1 then
		return
	end
	if EquipVisual_NEW_Material == -1 then
		return
	end
	if EquipVisual_NEW_Selection == -1 then
		return
	end
	
	Clear_XSCRIPT()
		Set_XSCRIPT_Function_Name("XSCRIPT")
		Set_XSCRIPT_ScriptID(045001)
		Set_XSCRIPT_Parameter(0, 3)
		Set_XSCRIPT_Parameter(1, EquipVisual_NEW_Index)
		Set_XSCRIPT_Parameter(2, EquipVisual_NEW_Material)
		Set_XSCRIPT_Parameter(3, EquipVisual_NEW_Selection)
		Set_XSCRIPT_ParamCount(4)
	Send_XSCRIPT()
	--EquipVisual_NEW_UpdateUI()
end

