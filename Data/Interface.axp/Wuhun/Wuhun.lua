				Kfs_Base_Value[i]:SetText(Property_Value)
			end
		end

		--SKills
		local KFS_Skill1 = string.sub(VoHon_Author,35,36)
		if KFS_Skill1 ~= "--" then
			KFS_Skill1 = tonumber(KFS_Skill1) + 1360;
