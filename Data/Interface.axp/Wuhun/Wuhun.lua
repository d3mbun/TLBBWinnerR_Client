function Wuhun_OnEvent(event)

	if event == "UI_COMMAND" and tonumber(arg0) == 74130101 then
		VoHon_Author = Get_XParam_STR(0)
		
		--PushDebugMessage(VoHon_Author);
		if IsWindowShow("Wuhun") then
			Wuhun_Update()
		end
	end

	if( event == "PLAYER_LEAVE_WORLD") then

		this:Hide();
		return;

	--elseif( event == "TOGGLE_WUHUN_PAGE") then
	elseif (event == "UI_COMMAND" and tonumber(arg0) == 20111211) then
		if(this:IsVisible()) then
			this:Hide();
			return;
		end
		
		Wuhun_Update()
		this:Show();

	elseif( event == "REFRESH_EQUIP" and this:IsVisible() ) then
		Variable:SetVariable("SelfUnionPos", Wuhun_Frame:GetProperty("UnifiedPosition"), 1);
		Wuhun_Update()

	end
end

