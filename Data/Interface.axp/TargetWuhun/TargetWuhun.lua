			local KFS_Attr = string.sub(VoHon_Author,VoHon_AttrEx_Pos[i][1],VoHon_AttrEx_Pos[i][2])

			if KFS_Attr ~= "---" then
				KFS_Attr = tonumber(KFS_Attr)
				TargetKfs_AttrEx_Text[i]:Show()
				TargetKfs_AttrEx_Value[i]:Show()
				
				local iText = VoHon_Option[KFS_Attr][1];
				local iValue = VoHon_Option[KFS_Attr][KFS_Base + 2];
				TargetKfs_AttrEx_Text[i]:SetText(iText)
				TargetKfs_AttrEx_Value[i]:SetText("+"..tostring(iValue))
			else
				TargetKfs_AttrEx_Text[i]:Hide()
				TargetKfs_AttrEx_Value[i]:Hide()
			end
		end
		
		--BaseAttr
		local Property_Value = 0;
		if ItemID == 10308001 or ItemID == 10308003 then
			for i = 1, 5 do
				Property_Value = VoHon_Property[KFS_Level][i]
				TargetKfs_Base_Original_Value[i]:SetText(Property_Value)
				
				Property_Value = VoHon_Property[KFS_Level][KFS_Base*5 + i]
				TargetKfs_Base_Value[i]:SetText(Property_Value)
			end
		else
			Property_Value = VoHon_Property[KFS_Level][1]
			TargetKfs_Base_Original_Value[2]:SetText(Property_Value)
			Property_Value = VoHon_Property[KFS_Level][KFS_Base*5 + 1]
			TargetKfs_Base_Value[2]:SetText(Property_Value)
			
			Property_Value = VoHon_Property[KFS_Level][2]
			TargetKfs_Base_Original_Value[1]:SetText(Property_Value)
			Property_Value = VoHon_Property[KFS_Level][KFS_Base*5 + 2]
			TargetKfs_Base_Value[1]:SetText(Property_Value)
			for i = 3, 5 do
				Property_Value = VoHon_Property[KFS_Level][i]
				TargetKfs_Base_Original_Value[i]:SetText(Property_Value)
				
				Property_Value = VoHon_Property[KFS_Level][KFS_Base*5 + i]
				TargetKfs_Base_Value[i]:SetText(Property_Value)
			end
		end
		
		
		--SKills
		local KFS_Skill1 = string.sub(VoHon_Author,35,36)
		if KFS_Skill1 ~= "--" then
			KFS_Skill1 = tonumber(KFS_Skill1) + 1360;
		end
		local KFS_Skill2 = string.sub(VoHon_Author,37,39)
		if KFS_Skill2 ~= "---" then
			KFS_Skill2 = tonumber(KFS_Skill2) + 1384;
		end
		local KFS_Skill3 = string.sub(VoHon_Author,40,41)
		if KFS_Skill3 ~= "--" then
			KFS_Skill3 = tonumber(KFS_Skill3) + 1516;
		end
		
		if KFS_Skill1 ~= "--" then
			theAction = GemCarve:UpdateProductAction( VoHon_Skill[KFS_Skill1][1] )
			TargetKfs_Skills[1]:SetActionItem(theAction:GetID());
		end
		
		if KFS_Skill2 ~= "---" then
			theAction = GemCarve:UpdateProductAction( VoHon_Skill[KFS_Skill2][1] )
			TargetKfs_Skills[2]:SetActionItem(theAction:GetID());
		end
		
		if KFS_Skill3 ~= "--" then
			theAction = GemCarve:UpdateProductAction( VoHon_Skill[KFS_Skill3][1] )
			TargetKfs_Skills[3]:SetActionItem(theAction:GetID());
		end
	end
end

